class Bowba() : CustomBow(
    name = "Bowba",
    customModelData = 0,
    description = listOf("You put bowba in your drink", "Bowba will get ruined"),
    levelRequirement = 38,
    material = Material.BOW,
    baseStats = CustomItemUtils.statMap(strength = 60.0, luck = 13.0),
    recipeList = recipes {
        recipe {
            shaped = true
            s2(RefinedMetal.tier(4))
            s3(Vine.tier(3))
            s4(Bone.tier(4))
            s6(Vine.tier(3))
            s8(RefinedMetal.tier(4))
            s9(Vine.tier(3))
            item { player, b ->
                val newItem = Bowba(if (b) 50 else Utils.randRarity())
                newItem.updateMeta(b)
                newItem
            }
        }
    },
) {

    constructor(quality: Int): this() {
        this.quality = quality
        this.rarity = Rarity.getFromInt(quality)
        this.serialize()
    }

    constructor(item: ItemStack): this() {
        this.item = item
        deserialize()
    }

}